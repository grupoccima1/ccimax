         <h2 class="futura-demi" style="text-align: center;">Aviso de privacidad</h2>

        <ol>
            <li>Definición.</li>
            <p>Documento legal que realiza una organización, para dar a conocer el tratamiento de datos personales del
                cliente, los cuales serán recopilados y almacenados debido a la relación comercial, así como dar a
                conocer los tipos de datos que se solicitarán y las medidas de seguridad que se tomarán en cuenta para
                que la información sensible proporcionada no llegue a terceros.</p>
            <li>Objetivo.</li>
            <p>Que el cliente pueda depositar su confianza en la organización al tener información detallada de cómo
                serán recabados y utilizados sus datos personales, así como la seguridad con la que se manejarán los
                mismos.</p>
            <li>Datos que serán recopilados por la organización.</li>
            <ul>
                <li>Nombre(s) y apellidos</li>
                <li>Domicilio</li>
                <li>Dirección de correo electrónico</li>
                <li>Número de teléfono </li>
                <li>Fecha de nacimiento</li>
            </ul>
            <li>Medios por los que se recabarán los datos.</li>
            <ul>
                <li>Campañas de marketing</li>
                <li>Facebook oficial de la organización</li>
                <li>Google ADS</li>
                <li>Cambaceo personal autorizado</li>
                <li>Cartera de clientes</li>
            </ul>

            <li>Temporalidad del uso de datos.</li>
            <p>Únicamente se solicitarán los datos de los clientes que hayan culminado todo el proceso inicial para
                formalizar la compraventa, es decir, hayan realizado el pago de su enganche y tengan en su posesión el
                contrato debidamente firmado por ambas partes, debido a esto, el uso de datos se limitará al tiempo que
                perduré la relación comercial entre el cliente y la organización, o cuando el cliente determine que ya
                no desea que su información sea utilizada.</p>

            <li>Consentimiento del uso de datos personales.</li>
            <p>El cliente proporcionará sus datos personales mediante una plantilla establecida por la organización, así
                mismo dará su autorización para el uso de su información compartida</p>
            <p>En cualquier momento el cliente podrá decidir retirar o cancelar su autorización, para que la
                organización deje de hacer uso de sus datos personales, mediante escrito.</p>
            <li>Finalidad de la recolección de datos.</li>
            <ul>
                <li>Creación y gestión de una base de datos de los clientes.</li>
                <li>Recordatorio de adeudos.</li>
                <li>Agradecimiento, felicitaciones o invitaciones en fechas especiales.</li>
                <li>Avances de proyectos.</li>
                <li>Información de la organización que pueda ser relevante para el cliente o la relación comercial.</li>
                <li>Aquellas otras finalidades que la organización establezca para mejoras del servicio al cliente</li>
            </ul>
            <li>Seguridad.</li>

            <p>Únicamente personal autorizado tendrá acceso a los datos proporcionados por el cliente y queda
                estrictamente prohibido proporcionar información a terceros para algún fin distinto a lo establecido en
                esta política, así mismo la información del cliente se encontrará cifrada mediante algoritmo AES, para
                mayor protección a los datos personales.</p>
            <li>Prohibiciones</li>
            <p>Solicitar información personal al cliente o de terceros como ideología, religión, orientación sexual, o
                cualquier otro que se relacione a la vida privada.</p>
            <p>Dar a conocer nuevos medios de pago, cantidades o acuerdos distintos a los establecidos mediante
                contrato.</p>
            <li>Responsabilidad.</li>
            <p>La organización se compromete a adoptar las medidas de seguridad necesarias para garantizar el correcto
                tratamiento de los datos personales proporcionados por el cliente, evitando alteraciones y perdidas, así
                como destinar un fin distinto a lo establecido en la presente.</p>
            <p>El cliente se compromete a que los datos personales proporcionados a la organización serán verídicos y en
                caso contrario será el único responsable de los perjuicios que se pudieran ocasionar, así mismo reconoce
                haber leído la presente política de privacidad y otorgar su consentimiento para que la organización dé
                el tratamiento a sus datos personales de acuerdo con los fines establecidos con anterioridad.</p>

        </ol>

